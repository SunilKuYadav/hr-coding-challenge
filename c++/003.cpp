// Data types in C++: int, float, double, char, bool

#include <iostream>
using namespace std;

int main() {
    int integerVar = 10;
    float floatVar = 5.5f;
    double doubleVar = 19.99;
    char charVar = 'A';
    bool boolVar = true;
    string strVar = "Hello, C++";

    cout << "Integer: " << integerVar << endl;
    cout << "Float: " << floatVar << endl;
    cout << "Double: " << doubleVar << endl;
    cout << "Character: " << charVar << endl;
    cout << "Boolean: " << boolVar << endl;
    cout << "String: " << strVar << endl;

    return 0;
}